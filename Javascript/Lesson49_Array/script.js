var someData = [10, 20, 30, 60 , 40, 15];
console.log(someData);
console.log("SORT METHOD   array.sort() ")
var sortsomeData = someData.sort();
console.log(sortsomeData);
console.log("REVERSE METHOD   array.reverse() ")
var reversesomeData = someData.reverse();
console.log(reversesomeData);
console.log("JOIN METHOD   array.join() ")
var joinsomeData = sortsomeData.join();
console.log(joinsomeData);

